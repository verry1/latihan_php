function ambil($url, $reff = null, $proxy = null, $safemode = false)
{
    if ($safemode === true) {
        sleep(5);
    } // safe mode, i dont want GOOGLE ban me..
    $im = curl_init($url);
    
    curl_setopt($im, CURLOPT_RETURNTRANSFER, 1);
    curl_setopt($im, CURLOPT_CONNECTTIMEOUT, 20);
    
    $headers   = array();
    $headers[] = 'Content-Type: application/x-www-form-urlencoded';
    
    curl_setopt($im, CURLOPT_HTTPHEADER, $headers);
    curl_setopt($im, CURLOPT_FOLLOWLOCATION, 1);
    curl_setopt($im, CURLOPT_HEADER, 1);
    if ($proxy != null) {
        curl_setopt($im, CURLOPT_HTTPPROXYTUNNEL, false);
        curl_setopt($im, CURLOPT_PROXY, $proxy);
    }
    
    curl_setopt($im, CURLOPT_SSL_VERIFYPEER, false);
    if ($reff != null) {
        curl_setopt($im, CURLOPT_REFERER, $reff);
    }
    
    return curl_exec($im);
    curl_close();
}


